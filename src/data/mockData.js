// ─── COMPUTED STATS ───────────────────────────
export function getStats() {
  const activeDeployments = botDeployments.filter(d => d.deployment_status === 'Deployed').length
  const openTickets = supportTickets.filter(t => t.ticket_status !== 'Resolved' && t.ticket_status !== 'Closed').length
  const criticalTickets = supportTickets.filter(t => t.priority === 'Critical' && t.ticket_status !== 'Resolved').length
  const inProgress = pendingWork.filter(w => w.work_status === 'In Progress').length
  const blocked = pendingWork.filter(w => w.work_status === 'Blocked').length
  const totalMRR = onboardingRecords.reduce((sum, r) => sum + r.contract_value, 0)
  const liveClients = onboardingRecords.filter(r => r.onboarding_stage === 'Live' || r.onboarding_stage === 'Complete').length
  const oauthExpired = botDeployments.filter(d => d.oauth_status === 'Expired' && d.deployment_status === 'Deployed').length

  return { activeDeployments, openTickets, criticalTickets, inProgress, blocked, totalMRR, liveClients, oauthExpired }
}
